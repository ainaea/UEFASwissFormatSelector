function confirmPot(isDeleteClicked) {
    var deleteSpan = 'potSpan';
    var confirmDeleteSpan = 'confirmPotSpan';

    if (isDeleteClicked) {
        $('#' + deleteSpan).hide();
        $('#' + confirmDeleteSpan).show();
    }
    else {
        $('#' + deleteSpan).show();
        $('#' + confirmDeleteSpan).hide();
    }
}
